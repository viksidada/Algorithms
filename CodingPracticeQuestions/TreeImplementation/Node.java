package com.CodingPracticeQuestions.TreeImplementation;

public class Node {

	Node left, right;
	private int data;
	
	public Node() {
		super();
		left = null;
		right = null;
		this.data = data;
	}
	public Node(int data) {
		super();
		this.data = data;
		left = right = null;
	}
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	
	
	
	

	

	
}
